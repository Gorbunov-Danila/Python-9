# Создаем словарь school
school = {
    '1а': 25,
    '1б': 30,
    '2б': 28,
    '6а': 22,
    '7в': 27,
    # Другие классы...
}

# Вносим изменения
# а) Изменение количества учащихся в одном из классов
school['1а'] = 26

# б) Появление нового класса
school['8г'] = 24

# в) Расформирование (удаление) другого класса
if '2б' in school:
    del school['2б']

# Вычисляем общее количество учащихся в школе
total_students = sum(school.values())

# Выводим обновленный словарь и общее количество учащихся
print("Обновленный словарь school:", school)
print("Общее количество учащихся в школе:", total_students)
